/**
 * Test failure for administrator registration with a weak password.
 *
 * This test attempts to register a new administrator account with a
 * password that is too weak for business logic (e.g., less than 10
 * characters and missing complexity requirements). The API is expected to
 * reject the operation and not issue administrator tokens.
 *
 * Steps:
 *
 * 1. Prepare registration payload (random email, weak password, random
 *    nickname)
 * 2. Attempt /auth/administrator/join POST request
 * 3. Assert that the request fails (using TestValidator.error)
 * 4. Validate no account is issued and correct error handling is performed
 */
export async function test_api_administrator_join_weak_password_failure(
  connection: api.IConnection,
) {
  const weakPassword = "abc123"; // Too short, lacks required complexity
  const requestBody = {
    email: `${RandomGenerator.alphabets(8)}@example.com`,
    password: weakPassword,
    nickname: RandomGenerator.name(),
  } satisfies IDiscussBoardAdministrator.IJoin;

  await TestValidator.error(
    "should reject registration with weak password",
    async () => {
      await api.functional.auth.administrator.join(connection, {
        body: requestBody,
      });
    },
  );
}

