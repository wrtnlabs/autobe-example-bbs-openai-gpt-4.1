/**
 * Attempt to update a discussBoard post as administrator with a
 * non-existent postId.
 *
 * This test validates that if an administrator tries to modify a post by
 * supplying a UUID that does not correspond to any existing post, the API
 * responds with an error (not found). The scenario uses a valid
 * administrator login context, but a random, guaranteed-nonexistent postId.
 * Input for the update is syntactically correct and follows business and
 * type constraints.
 *
 * Steps:
 *
 * 1. Register an administrator (random email, nickname, etc.) with the join
 *    API to provide proper authentication context.
 * 2. Attempt an update with a random UUID (not matching any real post) and
 *    valid update data.
 * 3. Assert that the update operation results in an error (i.e. API does not
 *    allow non-existent posts to be updated).
 */
export async function test_api_administrator_post_update_post_not_found(
  connection: api.IConnection,
) {
  // 1. Register administrator for authentication context.
  const adminJoinInput = {
    email: typia.random<string & tags.Format<"email">>(),
    password: RandomGenerator.alphaNumeric(12),
    nickname: RandomGenerator.name(),
  } satisfies IDiscussBoardAdministrator.IJoin;
  const admin = await api.functional.auth.administrator.join(connection, {
    body: adminJoinInput,
  });
  typia.assert(admin);

  // 2. Generate a postId (UUID) that is not present in the database.
  const missingPostId = typia.random<string & tags.Format<"uuid">>();

  // 3. Prepare valid update data.
  const updateBody = {
    title: RandomGenerator.paragraph({ sentences: 5 }),
    body: RandomGenerator.content({ paragraphs: 1 }),
    business_status: "public",
    tag_ids: [typia.random<string & tags.Format<"uuid">>()],
  } satisfies IDiscussBoardPost.IUpdate;

  // 4. Attempt to update the non-existent post.
  await TestValidator.error(
    "updating non-existent post should fail",
    async () => {
      await api.functional.discussBoard.administrator.posts.update(connection, {
        postId: missingPostId,
        body: updateBody,
      });
    },
  );
}

