import api from "@ORGANIZATION/PROJECT-api";
import type { IAuthorizationToken } from "@ORGANIZATION/PROJECT-api/lib/structures/IAuthorizationToken";
import type { IDiscussBoardAdministrator } from "@ORGANIZATION/PROJECT-api/lib/structures/IDiscussBoardAdministrator";
import type { IDiscussBoardGlobalAuditLog } from "@ORGANIZATION/PROJECT-api/lib/structures/IDiscussBoardGlobalAuditLog";
import type { IPage } from "@ORGANIZATION/PROJECT-api/lib/structures/IPage";
import type { IPageIDiscussBoardGlobalAuditLog } from "@ORGANIZATION/PROJECT-api/lib/structures/IPageIDiscussBoardGlobalAuditLog";

/**
 * Validate successful global audit log search by admin with filters.
 *
 * 1. Register a new admin account with unique email/nickname
 * 2. Login with admin credentials to set authentication context
 * 3. Prepare realistic search filters for actor_type, time window, and
 *    pagination
 * 4. Call global audit log search endpoint and assert response type
 * 5. Assert data array is present and pagination fields are correct
 */
export async function test_api_admin_audit_logs_search_success(
  connection: api.IConnection,
) {
  // 1. Register admin account
  const adminEmail = typia.random<string & tags.Format<"email">>();
  const adminPassword = RandomGenerator.alphaNumeric(12);
  const adminNickname = RandomGenerator.name();
  const joinRes = await api.functional.auth.administrator.join(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
      nickname: adminNickname,
    } satisfies IDiscussBoardAdministrator.IJoin,
  });
  typia.assert(joinRes);

  // 2. Login as admin to refresh authentication (tokens may be updated on login)
  const loginRes = await api.functional.auth.administrator.login(connection, {
    body: {
      email: adminEmail,
      password: adminPassword,
    } satisfies IDiscussBoardAdministrator.ILogin,
  });
  typia.assert(loginRes);

  // 3. Prepare search filters (actor_type, created_from/created_to, page/limit)
  const actorType = "admin";
  const now = new Date();
  const fiveMinutesAgo = new Date(now.getTime() - 5 * 60 * 1000).toISOString();
  const nowIso = now.toISOString();
  const reqBody = {
    actor_type: actorType,
    created_from: fiveMinutesAgo,
    created_to: nowIso,
    page: 1 satisfies number,
    limit: 10 satisfies number,
  } satisfies IDiscussBoardGlobalAuditLog.IRequest;

  // 4. Search global audit logs with the filters
  const pageResult =
    await api.functional.discussBoard.administrator.auditLogs.index(
      connection,
      { body: reqBody },
    );
  typia.assert(pageResult);

  // 5. Verify response structure: pagination fields and data array
  TestValidator.predicate(
    "pagination object should have current/limit/records/pages >= 0",
    pageResult.pagination.current >= 0 &&
      pageResult.pagination.limit >= 0 &&
      pageResult.pagination.records >= 0 &&
      pageResult.pagination.pages >= 0,
  );
  TestValidator.predicate(
    "data array should exist",
    Array.isArray(pageResult.data),
  );
}

